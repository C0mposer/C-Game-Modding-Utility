from termcolor import colored

def print_error(string: str):
    print(colored(string, "red"))
    
def print_green(string: str):
    print(colored(string, "green"))
def print_cyan(string: str):
    print(colored(string, "cyan"))
def print_purple(string: str):
    print(colored(string, "purple"))
def print_dark_grey(string: str):
    print(colored(string, "dark_grey"))